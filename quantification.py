def get_group(locus):
	ss_list = get_filename.ss_list(locus)
	with open(ss_list, 'r') as file:
		my_list = file.readlines()
	# get the length of the list
	list_length = len(my_list)

	hit_list = get_filename.hit_list(locus)
	with open(hit_list, 'r') as file:
		alleles_list = file.readlines()
	num_alleles = len(alleles_list)

	# Make phyloCode database
	if list_length < 5:
		if num_alleles <= 80:
			group = 'group1'
		else:
			group = 'group2'
	elif 5 <= list_length <= 80:
		group = 'group3'
	else:
		group = 'group4'

	write_log(locus, f"{group}\t{str(time.ctime())}\n")
	return(group)

def decide_order(locus, group, selection, df_Sdis, key_list, method, threads, ### medoid
						label_list,  medoid_list, medoid_assignment):
	if group == 'group1':
		standard_allel = group1(locus, method, threads)
	elif group == 'group2':
		standard_allel = group2(locus, method, threads, label_list, medoid_list, medoid_assignment)
	elif group == 'group3':
		standard_allel = group3(locus, selection, df_Sdis, key_list, method, threads)
	elif group == 'group4':
		standard_allel = group4(locus, selection, df_Sdis, key_list, method, threads, label_list, medoid_list, medoid_assignment)
	print('standard allele:', standard_allel)
	write_log(locus, f"standard allele\t{standard_allel}\n")
	return standard_allel

def get_lpnet_input_size(group, key_list, selection, medoid_list):
	if group == "group1":
		return len(key_list)
	elif group == "group2":
		return len(medoid_list)
	elif group == "group3":
		return len(selection)
	elif group == "group4":
		return len(medoid_list)
	else:
		return ""

def write_locus_processing_summary(locus, summary):
	directory_path = get_filename.dir_path(locus)
	tsv_file = os.path.join(directory_path, f"{locus}_processing_summary.tsv")

	fields = [
		"locus",
		"n_filtered_alleles",
		"n_snp_representatives",
		"group",
		"n_lpnet_input",
		"n_medoid_clusters",
		"standard_allele",
		"tree_method",
		"max_lpnet_taxa",
	]

	row = {field: summary.get(field, "") for field in fields}

	with open(tsv_file, "w", newline="") as f:
		writer = csv.DictWriter(f, fieldnames=fields, delimiter="\t")
		writer.writeheader()
		writer.writerow(row)

def calc_phylocode(locus, rm_temp, method, threads, locus_fasta, max_lpnet_taxa):
	write_log(locus, f"phylocode\tBegin\t{str(time.ctime())}\n")

	selection, df_Sdis, key_list = snps.snp_grouping(locus)
	ss_list = get_filename.ss_list(locus)
	selection_list = list(selection.keys())#selection seq_name list
	with open (ss_list,'w') as ofile:
		ofile.write('\n' .join(selection_list))
	group = get_group(locus)
	directory_path = get_filename.dir_path(locus)
	selected_align = get_filename.selected_align(locus)

	if group == 'group1' or group == 'group2':
		represent_fasta = directory_path + f"/{locus}_represent.fasta"
		fasta_select(locus_fasta, represent_fasta, ss_list) # Write representative alleles fasta
		in_fasta = get_filename.selected_align(locus)
	else: # group == 'group3' or group == 'group4'
		represent_fasta = directory_path + f"/{locus}_represent.fasta"
		fasta_select(locus_fasta, represent_fasta, ss_list) # Write represemtative alleles fasta
		in_fasta = directory_path + f"/{locus}_represent.aln"
		with open(in_fasta, "w") as out_handle:
			subprocess.run(
				["mafft", "--auto", represent_fasta],
				stdout=out_handle,
				check=True,
				text=True
			)

	make_distance_matrix(locus, in_fasta, method, threads) # Make tree and calculate distance matrix
	
	write_log(locus, f"run_lpnet\tBegin\t{str(time.ctime())}\n")
	label_list, medoid_list, medoid_assignment = run_lpnet(locus, group, max_lpnet_taxa) # Run lpnet
	write_log(locus, f"run_lpnet\tEnd\t{str(time.ctime())}\n")

	write_log(locus, f"decide_order\tBegin\t{str(time.ctime())}\n")
	standard_allel = decide_order(locus, group, selection, df_Sdis, key_list, method, threads, ### medoid
						label_list,  medoid_list, medoid_assignment)

	n_lpnet_input = get_lpnet_input_size(
			group,
			key_list,
			selection,
			medoid_list
		)

	summary = {
		"locus": locus,
		"n_filtered_alleles": len(key_list),
		"n_snp_representatives": len(selection),
		"group": group,
		"n_lpnet_input": n_lpnet_input,
		"n_medoid_clusters": len(medoid_assignment) if medoid_assignment else 0,
		"standard_allele": standard_allel,
		"tree_method": method,
		"max_lpnet_taxa": max_lpnet_taxa,
	}

	write_locus_processing_summary(locus, summary)

	write_log(locus, f"decide_order\tEnd\t{str(time.ctime())}\n")

	if rm_temp:
		del_tempfiles(locus)
	
	write_log(locus, f"phylocode\tEnd\t{str(time.ctime())}\n")

