def run_lpnet(locus, group, max_lpnet_taxa):
	# determine the order of alleles on a circle
	directory_path = get_filename.dir_path(locus)
	dmcsv = directory_path + f"/{locus}_dm.csv"
	dmlist = directory_path + f"/{locus}_dm.list"
	outnex = directory_path + f"/{locus}.nex"
	circleList_file = directory_path + f"/{locus}_circle.list"

	if group == 'group1' or group == 'group3':
		Lpnet.lpnet(dmcsv, dmlist, outnex)
		label_list, medoid_list, medoid_assignment = [], [], {}

		# For group1 and group3, lpnet input labels are all labels in dmlist.
		with open(dmlist, "r") as f:
			expected_labels = [line.strip() for line in f if line.strip()]

	else:  # group == 'group2' or group == 'group4'
		label_list, medoid_list, medoid_assignment = Lpnet.medoid_lpnet(
			dmcsv,
			dmlist,
			outnex,
			max_lpnet_taxa
		)

		# For group2 and group4, lpnet input labels are medoids only.
		expected_labels = medoid_list

	# Extract allele names from the Nexus file generated by lpnet.
	with open(outnex, 'r') as f:
		lines = f.readlines()

	filtered_lines = []
	for line in lines:
		if locus in line:
			parts = line.split("'")
			if len(parts) > 1:
				filtered_lines.append(parts[1])

	# Keep order, remove duplicates, and retain only expected labels.
	expected_set = set(expected_labels)
	cleaned = []
	seen = set()

	for x in filtered_lines:
		if x in expected_set and x not in seen:
			cleaned.append(x)
			seen.add(x)

	if not cleaned:
		raise ValueError(f"No allele order was extracted from lpnet output: {outnex}")

	missing = expected_set - set(cleaned)
	if missing:
		raise ValueError(
			f"{len(missing)} lpnet input alleles were not found in lpnet order. "
			f"Examples: {list(missing)[:5]}"
		)

	extra = set(filtered_lines) - expected_set
	if extra:
		write_log(
			locus,
			f"Warning\tignored_unexpected_lpnet_labels={len(extra)}\t"
			f"examples={list(extra)[:5]}\n"
		)

	with open(circleList_file, 'w') as f_out:
		for x in cleaned:
			f_out.write(x + '\n')

	if group not in ("group1", "group3"):
		save_medoid_assignment(locus, medoid_list, medoid_assignment)

	return label_list, medoid_list, medoid_assignment

def dismat4group(locus, mode, method, threads):

	# Preparing files and directories
	directory_path = get_filename.dir_path(locus)
	selected_align = get_filename.selected_align(locus)
	if mode == 'Mid':
		list_dir = directory_path + "/Mid_list"
		fasta_dir = directory_path + "/Mid_align"
		sub_tree_dir = directory_path + "/Mid_tree"
		sub_dm_dir = directory_path + "/Mid_dismat"
		sub_dmlist_dir = directory_path + "/Mid_dmlist"
	else:
		list_dir = directory_path + "/End_list"
		fasta_dir = directory_path + "/End_align"
		sub_tree_dir = directory_path + "/End_tree"
		sub_dm_dir = directory_path + "/End_dismat"
		sub_dmlist_dir = directory_path + "/End_dmlist"

	os.makedirs(sub_tree_dir, exist_ok=True)
	os.makedirs(sub_dm_dir, exist_ok=True)
	os.makedirs(sub_dmlist_dir, exist_ok=True)

	# Get group list in /End_list
	lsfile_list = os.listdir(list_dir)
	
	# make fasta for each group
	for filename in lsfile_list:
		# generate full path
		file_path = os.path.join(list_dir, filename)
		listfile_name = os.path.basename(filename)
		allele_name = os.path.splitext(listfile_name)[0]
		fastaout = fasta_dir + f"/{allele_name}.aln"
		listfile_path = list_dir + "/" + filename
		fasta_select(selected_align, fastaout, listfile_path)
	
	# Get file name in fasta directory containing fasta files for each group
	fsfile_list = os.listdir(fasta_dir)		
	for filename in fsfile_list:			
		# Make tree for each group
		fasta_path = os.path.join(fasta_dir, filename)
		fastafile_name = os.path.basename(filename)
		allele_name = os.path.splitext(fastafile_name)[0]
		outfile = sub_tree_dir + f"/{allele_name}"
		tr_file = make_tree(fasta_path, outfile, method, threads) # Run phylogenetic tree calculation
		# Make distance matrix files (dismat) for each group
		sub_dmcsv = sub_dm_dir + f"/{allele_name}_dm.csv"
		sub_dmlist = sub_dmlist_dir + f"/{allele_name}_dm.list"
		write_log(locus, f"make_distance_matrix_from_{os.path.basename(tr_file)}\tBegin\t{str(time.ctime())}\n")
		dismat_generate(tr_file , sub_dmcsv, sub_dmlist)
		write_log(locus, f"make_distance_matrix_from_{os.path.basename(tr_file)}\tEnd\t{str(time.ctime())}\n")
		
	return(list_dir, fasta_dir, sub_tree_dir, sub_dm_dir, sub_dmlist_dir)

