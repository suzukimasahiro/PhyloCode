def make_tree(fasta, tree_out, method, threads):
	if method == "RAxML":
		cmd = [
			"raxml-ng",
			"--msa", fasta,
			"--all",
			"--model", "GTR+G",
			"--bs-trees", "100",
			"--prefix", tree_out,
			"--redo",
			"--seed", "1",
			"--threads", str(threads)
		]
		subprocess.run(cmd, check=True, text=True)
		return tree_out + ".raxml.support"

	if method == "FastTree":
		tree_file = tree_out + ".tre"
		with open(tree_file, "w") as out_handle:
			subprocess.run(
				["FastTree", "-gtr", "-nt", "-seed", "1234", fasta],
				stdout=out_handle,
				check=True,
				text=True
			)
		return tree_file

	raise ValueError(f"Unknown tree method: {method}")

def dismat_generate(tree_file, dmcsv, dmlist):
	tree = Phylo.read(tree_file, 'newick')

	node_list = []

	for node in tree.get_terminals():
		node_list.append(node.name)

	with open(dmlist, 'w') as f:
		for item in node_list:
			f.write(item + '\n')
   	     

	ret_val = tree.get_terminals()
	num_node = tree.count_terminals()
	distance_ndarray = np.zeros((num_node, num_node))

	for combi in itertools.combinations(range(num_node), 2):  # Make distance matrix
		dist_temp = tree.distance(ret_val[combi[0]], ret_val[combi[1]])
		distance_ndarray[combi[0], combi[1]] = dist_temp
		distance_ndarray[combi[1], combi[0]] = dist_temp
	
	np.savetxt(dmcsv, distance_ndarray, delimiter=',')

